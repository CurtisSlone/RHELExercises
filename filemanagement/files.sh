# Create a file
touch newfile.txt

# Create a directory
mkdir newdir

# List files in the current directory
ls -l

# Display the content of a file
cat newfile.txt

# Copy a file
cp newfile.txt copyfile.txt

# Move (rename) a file
mv copyfile.txt movedfile.txt

# Remove a file
rm newfile.txt

# Remove a directory
rmdir newdir

